CREATE TABLE EMPLOYEE(
  ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  FIRSTNAME VARCHAR(50),
  LASTNAME VARCHAR(50),
  USERNAME VARCHAR(50) NOT NULL,
  ROLE VARCHAR(50),
  MANAGER_ID INT(9)
);

CREATE TABLE AGREEMENT(
  ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  MANAGER_ID INT(9) NOT NULL,
  EMPLOYEE_ID INT(9) NOT NULL,
  YEAR INT(9) NOT NULL,
  MONTH INT(9) NOT NULL,
  EVALUATORCONFIRMATION VARCHAR(50),
  EVALUEECONFIRMATION VARCHAR(50),
  FOREIGN KEY (MANAGER_ID) REFERENCES EMPLOYEE(ID),
  FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE(ID)
);

ALTER TABLE EMPLOYEE ADD FOREIGN KEY (MANAGER_ID) REFERENCES EMPLOYEE(ID);
